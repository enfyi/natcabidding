matched_bidders as (
  select b.id, b.first_name, b.last_name, b.email as existing_email, i.email as import_email
  from public.bidders b
  join email_import i
    on lower(trim(b.first_name)) = lower(trim(i.first_name))
   and lower(trim(b.last_name)) = lower(trim(i.last_name))
),
matched_staging as (
  select s.id, s.first_name, s.last_name, s.email as existing_email, i.email as import_email
  from public.staging_seniority_roster s
  join email_import i
    on lower(trim(s.first_name)) = lower(trim(i.first_name))
   and lower(trim(s.last_name)) = lower(trim(i.last_name))
)
select
  (select count(*) from email_import) as import_email_rows,
  (select count(*) from matched_bidders) as matched_bidders,
  (select count(*) from matched_bidders where existing_email is distinct from import_email) as bidders_to_update,
  (select count(*) from matched_staging) as matched_staging,
  (select count(*) from matched_staging where existing_email is distinct from import_email) as staging_to_update,
  (select count(*) from email_import i where not exists (
    select 1 from public.bidders b where lower(trim(b.first_name)) = lower(trim(i.first_name)) and lower(trim(b.last_name)) = lower(trim(i.last_name))
  )) as import_rows_not_in_bidders,
  (select count(*) from (
    select lower(trim(first_name)) as first_name, lower(trim(last_name)) as last_name, count(*)
    from public.bidders
    group by 1, 2
    having count(*) > 1
  ) duplicate_names) as duplicate_bidder_names;